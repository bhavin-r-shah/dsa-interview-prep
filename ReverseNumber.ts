    let temp = num;
    let counter = 0;
    // while(temp / 10 >= 1) { // AI gave this condition but it is wrong. For 7, temp / 10 = 0.7 which is < 1, so loop will not run and counter will be 0. But we want counter to be 1 for single digit numbers.
