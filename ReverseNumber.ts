    // for(let i = counter; i >= 0; i--) { // AI wrote this wrong despite my pseudo code which told to run from counter - 1 to 0. If we run from counter to 0, we will have an extra 0 at the end of the output. E.g. for input 7, counter will be 1, so loop will run for i = 1 and i = 0. For i = 1, output will be 7 * 10^1 = 70 and for i = 0, output will be 0 * 10^0 = 0. So final output will be 70 + 0 = 70 instead of 7.
